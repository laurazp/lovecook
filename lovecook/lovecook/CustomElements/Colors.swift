import Foundation
import UIKit

extension UIColor {
    // #acc3a6
    class func customLightGreen() -> UIColor {
        return UIColor(red:172/255, green:195/255, blue:166/255, alpha:1.0)
    }

    // #2c2c54
    class func customDarkBlue() -> UIColor {
        return UIColor(red: 44/255, green: 44/255, blue: 84/255, alpha:1.0)
    }
    
    // #48658D
    class func customBlue() -> UIColor {
        return UIColor(red: 72/255, green: 101/255, blue: 141/255, alpha:1.0)
    }
    
    // #f49d6e    (244,157,110)
    class func customOrange() -> UIColor {
        return UIColor(red: 244/255, green: 157/255, blue: 110/255, alpha:1.0)
    }
}
